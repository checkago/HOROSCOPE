def physics_laws_block(
    *,
    interaction_raw: str,
    bound_raw: str,
    result_line: str = "",
    human_coda: str = "",
    why: str = "",
    quantum: str = "",
    intimacy: str = "",
    synthesis: str = "",
    vulnerabilities: str = "",
) -> str:
    """Короткий слой «законы рядом с человеческим текстом» + сухие строки из данных."""
    blob = " ".join(
        t.lower()
        for t in (
            result_line,
            human_coda,
            why,
            quantum,
            intimacy,
            synthesis,
            vulnerabilities,
            interaction_raw,
            bound_raw,
        )
        if t
    )

    lines: list[str] = [
        "#### Физика рядом: законы и аналогии",
        "",
        "Это не замена текста выше, а **опора**: те же образы, но с названием закона и формулой там, где она уместна.",
        "",
        "- **Поверхностное натяжение и избыточное давление (Young–Laplace):** для сферической капли "
        "`Δp = 2σ/R` — мало «вещества», но форму держит **чёткая граница фаз**. Смойте различимость границы — "
        "капля распадается; так читается связка «натяжение — капля смысла».",
        "- **Теплопроводность (Фурье):** `q = −k∇T` — поток к градиенту температуры. «Жар» внимания и давление "
        "не проникают вглубь мгновенно; без пауз и **гаваней отдыха** растёт перегрев «на поверхности» и напряжение внутри.",
        "- **Свободная энергия Гиббса:** `ΔG = ΔH − TΔS` — при той же энтальпии рост энтропии канала делает процесс "
        "менее выгодным. Ясные правила «я — ты» и границы — способ **не раздувать энтропию** смешивания ролей в быту.",
        "- **Диффузия (закон Фика, 1-е приближение):** `J = −D∇c` — сглачивание резких контрастов идёт во времени; "
        "«резкие истины» смягчаются заботой, если есть **достаточно времени** и принятый градиент доверия.",
    ]

    if any(
        k in blob
        for k in (
            "суперпозиц",
            "куспид",
            "иде",
            "два режима",
            "две ветви",
            "коллапс",
        )
    ):
        lines.append(
            "- **Суперпозиция и измерение (постулаты квантовой механики):** до выбора сценария удобно думать о "
            "`|ψ⟩ = Σ cᵢ|ψᵢ⟩`, `Σ|cᵢ|² = 1`; «узкий» совместный вопрос похож на измерение — **сужает** доступные варианты "
            "до согласованного исхода (или до честного «мы не совпали»)."
        )

    if "резонанс" in blob:
        lines.append(
            "- **Резонанс контура:** для последовательного контура индуктивность–ёмкость частота "
            "`ω₀ ≈ 1/√(LC)`; близость «частот» усиливает отклик, рассогласование уводит энергию в **биения** "
            "и рассеяние — метафора общего ритма пары."
        )

    if "катализ" in blob or "реакц" in blob:
        lines.append(
            "- **Скорость реакции (Аррениус, качественно):** `k ∝ exp(−E_a/(RT))` — «химия» и совместный катализ "
            "растут, когда снижается **эффективный барьер** доверия и включена совместная среда, без утечки в чужие поля."
        )

    lines.extend(
        [
            "",
            "**Как записано в карточке модели (техническая строка, без перевода):**",
            "",
            f"- **Тип взаимодействия (данные):** `{interaction_raw.strip() or '—'}`",
            f"- **Связанное состояние (данные):** `{bound_raw.strip() or '—'}`",
        ]
    )

    return "\n".join(lines)
