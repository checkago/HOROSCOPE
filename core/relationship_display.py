def humanize_relationship_field(text: str) -> str:
    """Переводит строки из MD в язык, которым обычно говорят о паре."""
    if not text or not str(text).strip():
        return (text or "").strip()
    s = str(text).strip()
    lower = s.lower()
    for old, new in _PHRASES:
        if old in lower:
            s = re.sub(re.escape(old), new, s, flags=re.IGNORECASE)
            lower = s.lower()
    for old, new in _TOKENS:
        if old in lower:
            s = re.sub(re.escape(old), new, s, flags=re.IGNORECASE)
            lower = s.lower()
    s = re.sub(r"\s+", " ", s)
    s = re.sub(r"\s*([,.;:])\s*", r"\1 ", s).strip()
    return s.strip()


