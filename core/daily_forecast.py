def _merge_timing_section(markdown: str, paragraph: str) -> str:
    needle = "#### Окна дня\n\n"
    idx = markdown.find(needle)
    if idx == -1:
        return markdown
    tail_start = idx + len(needle)
    tail = markdown[tail_start:]
    next_hdr = tail.find("\n#### ")
    if next_hdr == -1:
        return markdown
    body = paragraph.strip().replace("\r\n", "\n")
    body = re.sub(r"\s+", " ", body).strip()
    if not body:
        return markdown
    return markdown[:tail_start] + body + tail[next_hdr:]


def _sanitize_timing_llm_text(raw: str) -> str | None:
    text = raw.strip()
    if text.startswith("```"):
        lines = text.splitlines()
        if lines and lines[0].strip().startswith("```"):
            lines = lines[1:]
        if lines and lines[-1].strip() == "```":
            lines = lines[:-1]
        text = "\n".join(lines).strip()
    parts: list[str] = []
    for line in text.splitlines():
        s = line.strip()
        if not s or s.startswith("#"):
            continue
        parts.append(s)
    if not parts:
        return None
    text = re.sub(r"\s+", " ", " ".join(parts)).strip()
    if len(text) < 25:
        return None
    if len(text) > 520:
        text = text[:517]
        if " " in text:
            text = text[: text.rindex(" ")]
        text = text.rstrip(",;— \t") + "…"
    return text


